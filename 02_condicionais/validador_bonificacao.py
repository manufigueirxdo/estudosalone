salario = float(input("Quanto é o seu salário?"))
if salario <= 0:
    print("Erro: salário inválido!")
else:
    bonus_um = salario * 0.20
    bonus_dois = salario * 0.10
    salario <= 3000
    salario_bruto = salario + bonus_um
    salario_bruto = salario + bonus_dois

    print(f"Seu salário desse mês é de R${salario_bruto}")
